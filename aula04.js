// Assignment Operators

// = simple assignment
// += combined addition
// -= combined subtraction
// *= combined multiplication
// /= combined division
// %= combined rest

let number = 24; // simple assignment
console.log(number);

number += 24; // combined addition
console.log(number);
number -= 2; // combined subtraction
console.log(number);
number /= 3; // combined division
console.log(number);
number *= 10; // combined multiplication
console.log(number);
number %= 2; // combined rest
console.log(number);